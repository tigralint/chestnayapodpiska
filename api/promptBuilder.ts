/**
 * Centralized prompt builder for AI claim generation.
 * Eliminates duplication between subscription and course prompt templates.
 */

interface PromptParams {
    entityName: string;
    entityLabel: string;
    financialDetails: string;
    tonePart: string;
    verbForms: string;
    specificParagraphs: string[];
}

export function buildClaimPrompt(params: PromptParams): string {
    const paragraphList = params.specificParagraphs
        .map(p => `   - ${p}`)
        .join('\n');

    return `Составь развернутый текст заявления о возврате средств.
ДАННЫЕ:
<user_input>
${params.financialDetails}
</user_input>

ИНСТРУКЦИЯ (СТРОГО):
1. Обязательно назови ${params.entityLabel} "${params.entityName}" по имени в тексте.
2. Ссылка на ст. 32 ЗоЗПП и ст. 782 ГК РФ.
3. Срок возврата — 10 дней (ст. 31 ЗоЗПП).
4. ${params.tonePart}
5. ${params.verbForms}
6. Текст должен содержать 4-5 отдельных абзацев (БЕЗ заголовков «Вступление», «Факты» и т.д. — просто сплошные абзацы текста):
${paragraphList}
7. Каждый абзац — не менее 2-3 предложений. Общий объём — минимум 200 слов.
8. НЕ ВЫДУМЫВАЙ данные (ИНН, расчётные счета, БИК, номера договоров). Вместо них ставь плейсхолдеры: [ВАШИ РЕКВИЗИТЫ], [ФИО], [№ ДОГОВОРА].

ФОРМАТ: ТОЛЬКО основной текст заявления. Без шапок, заголовков и подписей. Чистый текст. Абзацы разделяй пустой строкой.`;
}

export function buildSubscriptionPrompt(
    serviceName: string,
    amount: string,
    date: string,
    reason: string,
    tone: 'soft' | 'hard'
): string {
    const tonePart = tone === 'hard'
        ? 'Тон: Ультимативный. Упомяни Роспотребнадзор и суд.'
        : 'Тон: Вежливый и лояльный. Напиши, что я ценю сервис, но прошу возврат.';

    return buildClaimPrompt({
        entityName: serviceName,
        entityLabel: 'сервис',
        financialDetails: `- СЕРВИС: ${serviceName}\n- СУММА: ${amount} руб.\n- ДАТА: ${date}\n- ПРИЧИНА: ${reason}`,
        tonePart,
        verbForms: 'Используй формы: «пользовался(-ась)», «подписался(-ась)», «отменил(а)».',
        specificParagraphs: [
            'Кто я, когда и на что подписался.',
            'Что произошло, почему хочу вернуть деньги, подробности ситуации.',
            'Ссылки на конкретные статьи закона и их суть.',
            'Чётко сформулировать, чего именно требую (возврат суммы, сроки).',
            'Предупреждение о последствиях или вежливое завершение.',
        ],
    });
}

export function buildCoursePrompt(
    courseName: string,
    totalCost: string,
    percentCompleted: string,
    refund: string,
    tone: 'soft' | 'hard'
): string {
    const tonePart = tone === 'hard' ? 'Тон: Жесткий.' : 'Тон: Конструктивный.';

    return buildClaimPrompt({
        entityName: courseName,
        entityLabel: 'школу',
        financialDetails: `- ШКОЛА: ${courseName}\n- ОБЩАЯ ЦЕНА: ${totalCost} руб.\n- ПРОЙДЕНО: ${percentCompleted}%\n- СУММА К ВОЗВРАТУ: ${refund} руб.`,
        tonePart,
        verbForms: 'Используй формы: «приобрел(а)», «изучил(а)», «решил(а)».',
        specificParagraphs: [
            'Когда и какой договор был заключён, стоимость обучения.',
            `Сколько пройдено, почему решил(а) расторгнуть, конкретные претензии к качеству или формату.`,
            `Ссылки на ст. 32 ЗоЗПП, расчёт суммы к возврату.`,
            `Точная сумма возврата ${refund} руб., срок 10 дней.`,
            'Предупреждение о последствиях или вежливое завершение.',
        ],
    });
}

/**
 * Separate prompt for user-written custom reasons.
 * Two-phase approach: AI first evaluates legal validity, then generates or refuses.
 */
export function buildCustomReasonPrompt(
    serviceName: string,
    amount: string,
    date: string,
    customReason: string,
    tone: 'soft' | 'hard'
): string {
    const tonePart = tone === 'hard'
        ? 'Тон: Ультимативный. Упомяни Роспотребнадзор и суд.'
        : 'Тон: Вежливый и лояльный. Напиши, что я ценю сервис, но прошу возврат.';

    return `Ты — юрист-консультант по защите прав потребителей РФ.

ДАННЫЕ:
<user_input>
- СЕРВИС: ${serviceName}
- СУММА: ${amount} руб.
- ДАТА СПИСАНИЯ: ${date}
- ПРИЧИНА ПОЛЬЗОВАТЕЛЯ: "${customReason}"
</user_input>

ЗАДАЧА (строго по шагам):

ШАГ 1 — ОЦЕНКА ПРИЧИНЫ:
Определи, есть ли юридическое основание для возврата по ст. 32 ЗоЗПП / ст. 782 ГК РФ.
Валидные основания включают (но не ограничиваются):
- Неиспользование услуги после оплаты
- Забыл отменить автопродление / пробный период
- Не было уведомления о списании
- Навязанная услуга, подписка без явного согласия
- Технические проблемы, не позволяющие пользоваться сервисом
- Несоответствие услуги описанию или заявленному качеству
- Двойное списание
- Любая другая ситуация, при которой потребитель не получил оплаченную услугу

ШАГ 2 — ДЕЙСТВИЕ:

ЕСЛИ причина ВАЛИДНА — составь претензию:
1. Обязательно назови сервис "${serviceName}" по имени в тексте.
2. Ссылка на ст. 32 ЗоЗПП и ст. 782 ГК РФ.
3. Срок возврата — 10 дней (ст. 31 ЗоЗПП).
4. ${tonePart}
5. Используй формы: «пользовался(-ась)», «подписался(-ась)», «отменил(а)».
6. 4-5 абзацев (БЕЗ заголовков), каждый минимум 2-3 предложения, общий объём — минимум 200 слов.
7. НЕ ВЫДУМЫВАЙ данные. Используй плейсхолдеры: [ВАШИ РЕКВИЗИТЫ], [ФИО], [№ ДОГОВОРА].
ФОРМАТ: ТОЛЬКО чистый текст претензии. Без шапок, заголовков и подписей. Абзацы разделяй пустой строкой.

ЕСЛИ причина НЕВАЛИДНА (абсурдная, не имеет отношения к ЗоЗПП, нет оснований для возврата) —
начни ответ СТРОГО с маркера [ОТКАЗ] и далее вежливо объясни за 2-4 предложения,
почему данная ситуация не является основанием для претензии по закону.
Предложи пользователю подумать, есть ли другая причина, которая подходит под закон.`;
}

